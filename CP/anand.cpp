void solve()
{
    double n, t1, t2, k;
    cin >> n >> t1 >> t2 >> k;
    vector<pair<int, int>> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i].first >> a[i].second;
    }
    vector<pair<int, double>> ans(n);   
    for (int i = 0; i < n; i++)
    {
        double growth1 = t1*a[i].first*(100 - k);
        // cout<<growth<<endl;
        growth1 /= 100;
        growth1 += t2*a[i].second;
        double growth2 = t1*a[i].second*(100 - k);
        growth2 /= 100;
        growth2 += t2*a[i].first;
        ans[i] = {i, max(growth1, growth2)};
    }
    sort(all(ans), [&](pair<int, double> a, pair<int, double> b){
        return a.second > b.second;
    });
    for (int i = 0; i < n; i++)
    {
        cout << ans[i].first + 1 << " " << fixed << setprecision(2) << ans[i].second << endl;
    }
}
